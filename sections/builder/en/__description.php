<h2 class="item3">HTML 5 Builder</h2>

<p>Take the advantages of the HTML 5 Builder tool included with your landing template. Create and customize your layouts and content.</p>

<h3>Before Installing</h3>
<h4>Requirements</h4>

<p>To be able to use HTML Builder, you must have the following:</p>
<ul class="marked-list">
    <li>PHP</li>
    <li>Apache webserver (Windows servers will probably work, but are not supported)</li>
    <li>An FTP tool to upload the files</li>
</ul>

<h3>Installation / Using builder with your template</h3>
<p>Installing the HTML Builder script is simply a matter uploading the script files to a server/hosting account. Once uploaded, point your browser to said server/hosting and your script will work.</p>
<p>
    HTML Builder contains a variation of all sections of the template that you purchased. You can start building pages. It is enough to go to the builder folder on your hosting. (http://.../builder)
</p>


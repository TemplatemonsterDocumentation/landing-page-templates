<h2 class="item3">HTML 5 Билдер</h2>

<p>Ощутите все приемущества HTML 5 билдера входящего в Ваш landing template. Создвавайте и настраивайте ваш шаблон и контент.</p>

<h3>Перед установкой</h3>
<h4>Требования</h4>

<p>Для возможности работы с HTML билдером у Вы должны иметь:</p>
<ul class="marked-list">
    <li>PHP</li>
    <li>Apache webserver (Windows servers will probably work, but are not supported)</li>
    <li>An FTP tool to upload the files</li>
</ul>

<h3>Установка / Использование HTML билдера с Вашим шаблоном</h3>
<p>
    Для установки HTML билдера достаточно загрузить папку builder на Ваш сервер/хостинг.</p>
<p>
    Билдер содержит вариации всех секций приобретенного Вами шаблона. Можно приступать к формированию страницы. Достаточно перейти к папке builder на Вашем хостинге. (http://.../builder)
</p>

